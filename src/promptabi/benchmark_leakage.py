"""Detect benchmark-answer leakage in synthetic data (step 266).

Synthetic training data generated by a teacher model can accidentally memorize
and reproduce *evaluation benchmark* items, inflating eval scores through
contamination.  This module detects that leakage by comparing each synthetic
example against a benchmark set using two complementary signals:

* exact normalized-string overlap with a benchmark question or answer, and
* high n-gram (shingle) Jaccard overlap, which catches near-duplicates and
  lightly paraphrased copies.

It returns the specific synthetic examples that collide with specific benchmark
items, so the dataset can be decontaminated before training.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from enum import StrEnum

LEAKAGE_VERSION = "promptabi.benchmark-leakage.v1"


class LeakageKind(StrEnum):
    EXACT_MATCH = "exact-match"
    NGRAM_OVERLAP = "ngram-overlap"


def _normalize(text: str) -> str:
    return re.sub(r"\s+", " ", text.lower().strip())


def _shingles(text: str, n: int = 5) -> frozenset[str]:
    tokens = _normalize(text).split(" ")
    if len(tokens) < n:
        return frozenset({" ".join(tokens)}) if tokens else frozenset()
    return frozenset(" ".join(tokens[i : i + n]) for i in range(len(tokens) - n + 1))


def jaccard(a: frozenset[str], b: frozenset[str]) -> float:
    if not a or not b:
        return 0.0
    return len(a & b) / len(a | b)


@dataclass(frozen=True, slots=True)
class BenchmarkItem:
    item_id: str
    text: str


@dataclass(frozen=True, slots=True)
class SyntheticExample:
    example_id: str
    text: str


@dataclass(frozen=True, slots=True)
class LeakageFinding:
    kind: LeakageKind
    example_id: str
    benchmark_id: str
    score: float

    def to_dict(self) -> dict[str, object]:
        return {
            "kind": self.kind.value,
            "example_id": self.example_id,
            "benchmark_id": self.benchmark_id,
            "score": round(self.score, 4),
        }


@dataclass(frozen=True, slots=True)
class LeakageReport:
    version: str
    clean: bool
    findings: tuple[LeakageFinding, ...] = field(default=())

    def to_dict(self) -> dict[str, object]:
        return {
            "version": self.version,
            "clean": self.clean,
            "findings": [f.to_dict() for f in self.findings],
        }


def detect_leakage(
    synthetic: tuple[SyntheticExample, ...],
    benchmark: tuple[BenchmarkItem, ...],
    *,
    ngram: int = 5,
    threshold: float = 0.6,
) -> LeakageReport:
    findings: list[LeakageFinding] = []
    bench_norm = {b.item_id: _normalize(b.text) for b in benchmark}
    bench_shingles = {b.item_id: _shingles(b.text, ngram) for b in benchmark}

    for ex in synthetic:
        ex_norm = _normalize(ex.text)
        ex_shingles = _shingles(ex.text, ngram)
        for b in benchmark:
            if bench_norm[b.item_id] and bench_norm[b.item_id] in ex_norm:
                findings.append(
                    LeakageFinding(LeakageKind.EXACT_MATCH, ex.example_id, b.item_id, 1.0)
                )
                continue
            score = jaccard(ex_shingles, bench_shingles[b.item_id])
            if score >= threshold:
                findings.append(
                    LeakageFinding(
                        LeakageKind.NGRAM_OVERLAP, ex.example_id, b.item_id, score
                    )
                )

    return LeakageReport(
        version=LEAKAGE_VERSION,
        clean=not findings,
        findings=tuple(findings),
    )


def render_leakage_text(report: LeakageReport) -> str:
    lines = [
        f"PromptABI benchmark-answer leakage ({report.version})",
        f"result: {'CLEAN' if report.clean else 'CONTAMINATED'}",
    ]
    for f in report.findings:
        lines.append(
            f"  ! {f.kind.value}: {f.example_id} ~ {f.benchmark_id} "
            f"(score={f.score:.2f})"
        )
    return "\n".join(lines) + "\n"
